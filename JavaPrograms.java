package com.mycompany.javaprograms;

/**
 *
 * @author HP
 */
public class JavaPrograms {

    public static void main(String[] args) {
        
        /* the text to be printed must be inside the double quotes*/
        System.out.println("Hello World!");
        System.out.println("Learning java is very exciting....!");
        
        //the numbers to be printed need not be inside the double quotes
        System.out.println(41);
        
        /* basic arithmetic operations can also be done*/
        System.out.println(35+54);
        System.out.println(35-54);
        System.out.println(35*54);
        System.out.println(35/54);
        System.out.println(35%54);

    }
}
